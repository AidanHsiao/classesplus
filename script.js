function extend111() {
	if (s111 == 0) {
	document.getElementById("111").style.maxHeight = "60px";
	document.getElementById("111").style.padding = "20px";
	s111++;
	document.getElementById("symbol111").innerHTML = "-";
	} else {
	document.getElementById("111").style.maxHeight = "0px";
	document.getElementById("111").style.padding = "0px 20px";
	s111 = 0;
	document.getElementById("symbol111").innerHTML = "+";
	}
}

